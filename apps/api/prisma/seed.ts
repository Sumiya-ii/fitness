async function main() {
  console.log(`Seeding ${FOODS.length} foods...`);

  let created = 0;
  let skipped = 0;

  for (const food of FOODS) {
    const existing = await prisma.food.findFirst({
      where: { normalizedName: food.name },
    });

    if (existing) {
      skipped++;
      continue;
    }

    await prisma.food.create({
      data: {
        normalizedName: food.name,
        locale: 'mn',
        sourceType: 'admin',
        status: 'approved',
        servings: {
          create: food.servings.map((s) => ({
            label: s.label,
            labelMn: s.labelMn,
            gramsPerUnit: s.grams,
            isDefault: s.isDefault ?? false,
          })),
        },
        nutrients: {
          create: {
            caloriesPer100g: food.calories,
            proteinPer100g: food.protein,
            carbsPer100g: food.carbs,
            fatPer100g: food.fat,
            fiberPer100g: food.fiber ?? 0,
            sugarPer100g: 0,
            sodiumPer100g: 0,
            saturatedFatPer100g: 0,
          },
        },
        localizations: {
          create: [
            { locale: 'mn', name: food.nameMn },
            { locale: 'en', name: food.name },
          ],
        },
        aliases: {
          create: [{ alias: food.nameMn.toLowerCase(), locale: 'mn' }],
        },
      },
    });

    created++;
  }

  console.log(`Seed complete: ${created} created, ${skipped} skipped (already exist).`);
}

main()
  .catch((e) => {
    console.error('Seed failed:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
  });
